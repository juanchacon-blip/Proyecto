    private void limpiarCampos() {
        txtId.setText("");
        txtNombre.setText("");
        txtEstado.setText("");
        txtPrecio.setText("");
        chkEstadoDeVenta.setSelected(false);
        txtNombre.requestFocus();
    }
    
    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
                try {
            // **Validar campos obligatorios
            if (txtNombre.getText().trim().isEmpty() || txtEstado.getText().trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Nombre y Estado son campos obligatorios!");
                return;
            }
            
            if (txtId.getText().trim().isEmpty() || txtPrecio.getText().trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, "ID y Precio son campos obligatorios!");
                return;
            }
            
            // Obtener valores
            int id = Integer.parseInt(txtId.getText().trim());
            String nombre = txtNombre.getText().trim();
            String estado = txtEstado.getText().trim();
            boolean estadoDeVenta = chkEstadoDeVenta.isSelected();
            double precio = Double.parseDouble(txtPrecio.getText().trim());
            
            // Validar valores positivos
            if (id <= 0) {
                JOptionPane.showMessageDialog(this, "El ID debe ser un nÃºmero positivo!");
                return;
            }
            
            if (precio < 0) {
                JOptionPane.showMessageDialog(this, "El precio no puede ser negativo!");
                return;
            }
            
            // Crear y guardar mascota
            Mascota mascota = new Mascota(nombre, estado, id, estadoDeVenta, precio);
            boolean respuesta = ServicioMascota.grabarMascota(mascota);
            
            if (respuesta) {
                JOptionPane.showMessageDialog(this, "Mascota adicionada exitosamente!");
                limpiarCampos();
            } else {
                JOptionPane.showMessageDialog(this, "Error al adicionar la mascota!");
            }
            
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Error en formato de nÃºmeros. Verifique ID y Precio.");
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Error inesperado: " + ex.getMessage());
            ex.printStackTrace();
        }

    }//GEN-LAST:event_btnGuardarActionPerformed

    
    
    
    
