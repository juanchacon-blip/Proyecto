    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed


    int registroAModificar = Integer.parseInt(txtBuscar.getText().trim());
    long tamañoRegistro = 47;

    try (RandomAccessFile file = new RandomAccessFile("data//mascota.dat", "rw")) {
        // Calcular posición del registro
        long posicion = (registroAModificar - 1) * tamañoRegistro;
        file.seek(posicion);
        
        // Leer los datos actuales del registro
        String nombreActual = file.readUTF();
        String estadoActual = file.readUTF();
        int idActual = file.readInt();
        boolean estadoDeVentaActual = file.readBoolean();
        double precioActual = file.readDouble();
        
        // Volver al inicio del registro para modificar
        file.seek(posicion);
        
        // Mantener nombre e ID originales (no se pueden modificar)
        file.writeUTF(nombreActual);
        
        // Obtener nuevos valores de los campos de texto
        String nuevoEstado = txtEstado.getText().trim();
        boolean nuevoEstadoDeVenta = Boolean.parseBoolean(txtEstadoDeVenta.getText().trim());
        double nuevoPrecio = Double.parseDouble(txtPrecio.getText().trim());
      
        
     
        file.writeUTF(nuevoEstado);          
        file.writeInt(idActual);             
        file.writeBoolean(nuevoEstadoDeVenta); 
        file.writeDouble(nuevoPrecio);       
        
    } catch (Exception ex) {}
    }//GEN-LAST:event_btnActualizarActionPerformed

