    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed

    int id;
    String nombre, estado;
    boolean estadoDeVenta;
    double precio;
   
    int  n = Integer.parseInt(txtBuscar.getText().trim());
    long tamañoRegistro = 47;
    try (RandomAccessFile file = new RandomAccessFile("data//mascota.dat", "r")) { 
  
        
    
        int indice = n - 1;
       
        long posicion = indice * tamañoRegistro;
        
       
        file.seek(posicion);

        // Leemos los datos en el mismo orden en que fueron escritos:
        
        nombre = file.readUTF();
        estado = file.readUTF();
        id = file.readInt();  
        estadoDeVenta = file.readBoolean();
        precio = file.readDouble(); 

        // Mostramos los datos leídos en los campos de texto de la interfaz
        // Usamos .trim() para eliminar espacios extra agregados por formatearCadena
        txtNombre.setText(nombre.trim());
        txtEstado.setText(estado.trim());
        txtId.setText(String.valueOf(id));
        txtEstadoDeVenta.setText(String.valueOf(estadoDeVenta));
        txtPrecio.setText(String.valueOf(precio));

    } catch (Exception ex) {
        // Captura cualquier error durante la lectura (archivo no encontrado, permisos, etc.)
        System.out.println("Error al leer el registro " + n + ": " + ex.getMessage());
        ex.printStackTrace(); // Muestra el error completo en consola para depuración
    }
    }//GEN-LAST:event_btnBuscarActionPerformed

