    private void btnListarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnListarActionPerformed
       List<Mascota> mascotas = ServicioMascota.listarMascotas();
        
        DefaultTableModel model = (DefaultTableModel) tabMascotas.getModel();
        for(Mascota e : mascotas){
           
            model.addRow(new Object[]{e.getNombre(), e.getEstado(), e.getId(), e.isEstadoDeVenta(), e.getPrecio()});
        }
       
       
    }//GEN-LAST:event_btnListarActionPerformed

